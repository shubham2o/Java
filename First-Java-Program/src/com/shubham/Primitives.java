package com.shubham;

public class Primitives {
    public static void main(String[] args) {
        // primitive is any data-type that you cannot break down further.
        int rollNo = 64;    // 4 bytes
        long largeIntegerValues = 1234567890L;  // 8 bytes
        char letter = 'r';
        float percentage = 98.67f;  // 4 bytes
        double largeDecimalNumbers = 12345.67890;   // 8 bytes
        boolean check = true;   // or false

        // String is not primitive
        String name = "Shubham";
    }
}